
let myArray = [];
//Med push hamnar värdet sist i arrayen
myArray.push(12);
myArray.push(13);

//Med unshift hamnar värdet först i arrayen
myArray.unshift(10);
myArray.unshift(9);

//Man kan även ta bort från båda hållen
myArray.pop(); //Tar bort sista positionen dvs 13

myArray.shift(); //Tar bort första positionen dvs 9

